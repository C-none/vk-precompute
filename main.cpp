	static std::array<std::array<double, HEIGHT / 2>, WIDTH / 2> solid_angle_table;

	constexpr static void precompute_solid_angle()
	{
		using namespace std;
		using namespace glm;
		if constexpr (ENABLE_CORRECTION) {
			auto yoko = [&](const int32_t x) {return normalize(vec3{ 1,0,-2. * x / WIDTH }); };
			auto tate = [&](const int32_t y) {return normalize(vec3{ 0,1,-2. * y / HEIGHT });  };
			for (int32_t x = 0; x < WIDTH / 2; ++x)
				for (int32_t y = 0; y < HEIGHT / 2; ++y) {
					array<vec3, 5>angle{ yoko(x),tate(y),-yoko(x + 1),-tate(y + 1),yoko(x) };
					solid_angle_table[x][y] = -2 * numbers::pi;
					for (int i = 0; i < 4; ++i)
						solid_angle_table[x][y] += numbers::pi - acos((double)dot(angle[i], angle[i + 1]));
				}
		}
	}
