glm::vec3 get_pos(uint64_t id)
{
	float x{ (dst.x - src.x) / step.x }, y{ (dst.y - src.y) / step.y }, z{ (dst.z - src.z) / step.z };
	return { x * (id / ((step.y + 1) * (step.z + 1))) + src.x,y * (id % ((step.y + 1) * (step.z + 1)) / (step.z + 1)) + src.y,z * (id % (step.z + 1)) + src.z };
}

